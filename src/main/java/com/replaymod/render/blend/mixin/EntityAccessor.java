package com.replaymod.render.blend.mixin;

import net.minecraft.entity.Entity;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.gen.Accessor;

@Mixin(Entity.class)
public interface EntityAccessor {
    @Accessor
    double getPrevPosX();
    @Accessor
    double getPrevPosY();
    @Accessor
    double getPrevPosZ();
    @Accessor
    double getPosX();
    @Accessor
    double getPosY();
    @Accessor
    double getPosZ();
}
